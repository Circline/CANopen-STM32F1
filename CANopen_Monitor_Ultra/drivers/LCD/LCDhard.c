void DrawLine(u16 x_str,u16 y_str, u16 x_end,u16 y_end, int Color)
{
	unsigned int i=0;
	if(x_str==x_end)
	{
		BlockWrite(x_str,x_end,y_str,y_end);
		for(i=0;i<y_end-y_str-1;i++)
		LCD_DATA = Color;
	}
	else if(y_str==y_end)
	{
		BlockWrite(x_str,x_end,y_str,y_end);
		for(i=0;i<x_end-x_str-1;i++)
		LCD_DATA = Color;		
	}
	else
	{
		unsigned short x=x_str,y=y_str;
		float err=0.0f,gain=0.0f,fy=(float)y_str;
		err = (x_end - x_str);
		gain =  (float)((short)y_end - (short)y_str) / err;
		{	
			for(x=x_str;x<x_end;x++)
			{
				BlockWrite(x,x,y,y);
				LCD_DATA = Color;
				fy+=gain;
				y =(unsigned short)(short)fy ;
			}
			
		}
	}
	
}

